#include "libft.h"

void	ft_swap(int *nbr1, int *nbr2)
{
	int	tmp;

	if (!nbr1 || !nbr1)
		return ;
	tmp = *nbr1;
	*nbr1 = *nbr2;
	*nbr2 = tmp;
}
